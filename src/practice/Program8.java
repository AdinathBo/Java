package practice;

public class Program8 {

	public static void main(String[] args) {
		int n=121;
		switch (n) {
		case 100:
			  System.out.println(" one"+" zero"+" zero");
			  break;
		case 240:
			  System.out.println(" two"+" four"+" zero");
			  break;
		case 523:
			  System.out.println(" five"+" two"+" three");
			  break;
		case 121:
			  System.out.println(" one"+" two"+" one");
			  break;
			  default:
				  System.out.println("Invalid Number");
		}
	}
}
