package jbk;

public class Method_Overloading {
          void add(int a,int b) {
        	  int r=a+b;
        	  System.out.println(r);  
          }
          void add(int a,int b,int c) {
        	  int s=a+b+c;
        	  System.out.println(s);
          }
          void add(float a,float b) {
        	  float t=a+b;
        	  System.out.println(t);
          }
}
